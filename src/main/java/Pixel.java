
public class Pixel {
   private int X;
   private int Y;
  public Pixel(int incX, int incY){
        this.X = incX;
        this.Y = incY;
    }

    public int getX() {
        return X;
    }

    public void setX(int x) {
        X = x;
    }

    public int getY() {
        return Y;
    }

    public void setY(int y) {
        Y = y;
    }
}